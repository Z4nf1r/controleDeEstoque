    private void bntEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntEntrarActionPerformed
        // TODO add your handling code here:
        
        if(txtLogin.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Digite um Login!");
            return;
        }
        
        if(txtSenha.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Digite uma Senha!");
            return;
        }
        
        Usuario usuario = new Usuario();
        usuario.setLogin(txtLogin.getText());
        usuario.setSenha(txtSenha.getText());

        UsuarioImpl usuarioImpl = new UsuarioImpl();
        usuarioImpl.validaLogon(usuario);
        
        
        Menu menu = new Menu();
        menu.setVisible(true);
        dispose();
        
    }//GEN-LAST:event_bntEntrarActionPerformed

