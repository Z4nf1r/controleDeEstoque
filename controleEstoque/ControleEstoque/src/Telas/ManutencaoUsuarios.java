    private void BotaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoSalvarActionPerformed
        // TODO add your handling code here:
        
        Usuario usuario = new Usuario();

        if (TextIDusuarios.getText().equals("")) {
            usuario.setId_usuarios(0);
        } else {
            usuario.setId_usuarios(Integer.parseInt(TextIDusuarios.getText()));
        }
        usuario.setNome(TextNome.getText());
        usuario.setLogin(TextLogin.getText());
        usuario.setSenha(jPasswordField1.getText());
        usuario.setDatanascimento(jDateChooser1.getDate());
        usuario.setDataativacao(jDateChooser2.getDate());
        usuario.setDatadesativacao(jDateChooser3.getDate());
        if (jComboBox1.getSelectedIndex() == 0) {
            usuario.setSituacao("A");
        } else {
            usuario.setSituacao("I");
        }

        UsuarioImpl usuarioImpl = new UsuarioImpl();
        usuarioImpl.inserirUsuario(usuario);
        usuarioImpl.popularTabela(TabelaUsuarios);
    }                                        

