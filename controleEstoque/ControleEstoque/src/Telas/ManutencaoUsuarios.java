    private void BotaoAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoAltActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel tabelaUsuario = (DefaultTableModel) TabelaUsuarios.getModel();
        int row = TabelaUsuarios.getSelectedRow();

        String ValorSelecionado = "";
        if (row > -1) {
            ValorSelecionado = tabelaUsuario.getValueAt(row, 0).toString();
        }

        if (ValorSelecionado.equals("")) {
            JOptionPane.showMessageDialog(null, "Selecione um usuário para poder alterar!");
            return;
        } else {

            Usuario usuario = new Usuario();
            usuario.setId_usuarios(Integer.parseInt(ValorSelecionado));

            UsuarioImpl usuarioImpl = new UsuarioImpl();
            usuarioImpl.pesquisarUsuario(usuario);

            TextIDusuarios.setText(usuario.getId_usuarios().toString());
            TextNome.setText(usuario.getNome());
            jDateChooser2.setDate(usuario.getDatanascimento());
            jTextField1.setText(usuario.getLogin());
            jPasswordField1.setText(usuario.getLogin());

            if (usuario.getSituacao().equals("A")) {
                jComboBox1.setSelectedIndex(0);
            } else if (usuario.getSituacao().equals("I")) {
                jComboBox1.setSelectedIndex(1);
            }

            jDateChooser2.setDate(usuario.getDataativacao());
            jDateChooser3.setDate(usuario.getDatadesativacao());

            jTabbedPane1.setSelectedIndex(1);

        }
        
        Manutencao.setSelectedIndex(1);
        
    }//GEN-LAST:event_BotaoAltActionPerformed

    private void BotaoExActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoExActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel tabelaUsuario = (DefaultTableModel) TabelaUsuarios.getModel();
        int row = TabelaUsuarios.getSelectedRow();

        String ValorSelecionado = "";
        if (row > -1) {
            ValorSelecionado = tabelaUsuario.getValueAt(row, 0).toString();
        }

        if (ValorSelecionado.equals("")) {
            JOptionPane.showMessageDialog(null, "Selecione um usuário para excluir!");
            return;
        } else {
            Usuario usuario = new Usuario();
            usuario.setId_usuarios(Integer.parseInt(ValorSelecionado));
            
            UsuarioImpl usuarioImpl = new UsuarioImpl();
            usuarioImpl.excluirUsuario(usuario);
            usuarioImpl.popularTabela(TabelaUsuarios);
        }
        
    }//GEN-LAST:event_BotaoExActionPerformed

